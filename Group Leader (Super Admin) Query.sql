-- see students, their roles, tasks, and task labels
select at.student_id, r.name as 'role', at.task_id, t.title, t.description from student s
	inner join assign_role ar on s.id = ar.student_id
    inner join role r on r.id = ar.role_id
	inner join assign_task at on s.id = at.student_id
    inner join task t on t.id = at.task_id;
    
-- see  the comments of each task and who commented it, if ever there's any.
select t.id, t.title, t.description, c.note as 'comment', tc.student_id as 'commenter' from comment c
	inner join task_comment tc on c.id = tc.comment_id
    right join task t on t.id = tc.task_id;
    
